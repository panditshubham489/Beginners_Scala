package NewCourse

object valuevariabletype extends  App{

  var x: Int = 42
  println(x)

  var y = 67
  println(y)

  val bString: String = "hello"
  println(bString)
  val anotherString = "nice"
  println(anotherString)
  val SBoolean: Boolean = false
  val aChar: Char = 's'
  val anInt: Int = x
  val aShort: Short = 1234
  val Long: Long = 8077022600L
  val aFloat: Float = 1.0f
  val aDouble: Double = 3.14
  println(SBoolean)
  println(aChar)
  println(anInt)
  println(aShort)
  println(Long)
  println(aFloat)
  println(aDouble)

  // Variable
  var aVariable: Int = x
  //aVariable =5
  println(aVariable)
}
